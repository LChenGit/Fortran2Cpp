MODULE SHAPEFUNCEAS__genmod
  IMPLICIT NONE
  CONTAINS
    SUBROUTINE SHAPEFUNCEAS(DETJ, XII, ETAI, ZETAI)
      REAL(KIND=8), INTENT(OUT) :: DETJ
      REAL(KIND=8), INTENT(IN) :: XII, ETAI, ZETAI
      DETJ = XII*ZETAI - ETAI*2.0  ! Simplified operation for demonstration
    END SUBROUTINE SHAPEFUNCEAS
END MODULE SHAPEFUNCEAS__genmod

PROGRAM test_SHAPEFUNCEAS
  USE SHAPEFUNCEAS__genmod
  IMPLICIT NONE
  REAL(KIND=8) :: DETJ, XII, ETAI, ZETAI

  XII = 3.0
  ETAI = 4.0
  ZETAI = 5.0
  CALL SHAPEFUNCEAS(DETJ, XII, ETAI, ZETAI)

  PRINT *, "Fortran DETJ: ", DETJ
END PROGRAM test_SHAPEFUNCEAS