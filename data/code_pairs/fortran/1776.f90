PROGRAM testPA_FROM_MB
  IMPLICIT NONE
  REAL :: result
  REAL, PARAMETER :: BADDATA = -999.0
  REAL, PARAMETER :: MB = 101.3

  result = PA_FROM_MB(MB, BADDATA)
  PRINT *, 'Test with valid data:', result
  result = PA_FROM_MB(BADDATA, BADDATA)
  PRINT *, 'Test with BADDATA:', result

CONTAINS

  REAL FUNCTION PA_FROM_MB(MB, BADDATA)
    IMPLICIT NONE
    REAL, INTENT(IN) :: MB, BADDATA

    IF (MB .NE. BADDATA) THEN
      PA_FROM_MB = MB * 100.
    ELSE
      PA_FROM_MB = BADDATA
    END IF
  END FUNCTION PA_FROM_MB

END PROGRAM testPA_FROM_MB