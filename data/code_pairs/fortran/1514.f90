SUBROUTINE BSPARM(MCALL, AC1, AC2, IT1, IT2)
      IMPLICIT REAL*8 (A-H,O-Z)
      PARAMETER (MXDIM = 50, NDMX = 50 )
      COMMON /BPARM1/ XL(MXDIM),XU(MXDIM),NDIM,NWILD,
     .               IG(MXDIM),NCALL
      COMMON /BPARM2/ ACC1,ACC2,ITMX1,ITMX2

      INTEGER MCALL, IT1, IT2
      REAL*8 AC1, AC2

      NCALL = MCALL
      ACC1  = AC1
      ACC2  = AC2
      ITMX1 = IT1
      ITMX2 = IT2

      RETURN
      END