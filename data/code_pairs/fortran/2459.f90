MODULE SCASUM_MOD
  IMPLICIT NONE
CONTAINS
  REAL FUNCTION SCASUM(N,CX,INCX)
    INTEGER, INTENT(IN) :: N, INCX
    COMPLEX, INTENT(IN) :: CX(*)
    REAL :: STEMP
    INTEGER :: I, NINCX

    SCASUM = 0.0
    STEMP = 0.0
    IF (N.LE.0 .OR. INCX.LE.0) RETURN
    IF (INCX.EQ.1) THEN
       DO I = 1,N
          STEMP = STEMP + ABS(REAL(CX(I))) + ABS(AIMAG(CX(I)))
       END DO
    ELSE
       NINCX = N*INCX
       DO I = 1,NINCX,INCX
          STEMP = STEMP + ABS(REAL(CX(I))) + ABS(AIMAG(CX(I)))
       END DO
    END IF
    SCASUM = STEMP
  END FUNCTION SCASUM
END MODULE SCASUM_MOD