SUBROUTINE ADDJUT (MXND, XN, YN, LXK, KXL, NXL, LXN, ANGLE, LNODES, XNEW, YNEW, NNN, LLL, NOLD, NLOOP, JUTTED)
    INTEGER, INTENT(IN) :: MXND
    REAL, DIMENSION(MXND), INTENT(INOUT) :: XN, YN
    INTEGER, DIMENSION(4, MXND), INTENT(INOUT) :: LXK
    INTEGER, DIMENSION(2, 3*MXND), INTENT(INOUT) :: KXL
    INTEGER, DIMENSION(2, 3*MXND), INTENT(INOUT) :: NXL
    INTEGER, DIMENSION(4, MXND), INTENT(INOUT) :: LXN
    REAL, DIMENSION(MXND), INTENT(INOUT) :: ANGLE
    INTEGER, DIMENSION(7, MXND), INTENT(INOUT) :: LNODES
    REAL, INTENT(IN) :: XNEW, YNEW
    INTEGER, INTENT(INOUT) :: NNN, LLL, NOLD, NLOOP
    LOGICAL, INTENT(INOUT) :: JUTTED

    NNN = NNN + 1
    XN(NNN) = XNEW
    YN(NNN) = YNEW

    LLL = LLL + 1
    NXL(1, LLL) = NNN
    NXL(2, LLL) = NOLD

    DO 100 I = 1, 4
        LXN(I, NNN) = 0
100 CONTINUE

    KXL(1, LLL) = 0
    KXL(2, LLL) = 0

    LNODES(1, NNN) = 0
    LNODES(2, NNN) = NOLD
    LNODES(3, NNN) = NOLD
    LNODES(4, NNN) = -1
    LNODES(5, NNN) = LLL

    LNODES(1, NOLD) = 0
    LNODES(3, NOLD) = NNN
    LNODES(5, NOLD) = LLL

    NLOOP = NLOOP + 2
    JUTTED = .TRUE.

    RETURN
END