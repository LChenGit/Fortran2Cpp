LOGICAL FUNCTION DGENND (M, N, A, LDA)
  INTEGER M, N, LDA
  DOUBLE PRECISION A(LDA, *)
  DOUBLE PRECISION   ZERO
  PARAMETER          (ZERO = 0.0D0)
  INTEGER I, K
  INTRINSIC MIN
  K = MIN(M, N)
  DO I = 1, K
     IF (A(I, I) .LT. ZERO) THEN
        DGENND = .FALSE.
        RETURN
     END IF
  END DO
  DGENND = .TRUE.
  RETURN
END FUNCTION DGENND