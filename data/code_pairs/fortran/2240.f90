MODULE multiroute
  IMPLICIT NONE
  INTEGER, PARAMETER :: SP = KIND(1.0) ! Single precision kind
  INTEGER, PARAMETER :: NTDH_MAX = 100 ! Assumed constant for example

  TYPE RUNOFF
    REAL(SP) :: Q_INSTNT
    REAL(SP) :: Q_ROUTED
    REAL(SP) :: Q_ACCURATE
  END TYPE RUNOFF

  REAL(SP), DIMENSION(NTDH_MAX) :: FUTURE
  TYPE(RUNOFF), DIMENSION(:), POINTER :: AROUTE
  TYPE(RUNOFF), DIMENSION(:,:,:), ALLOCATABLE :: AROUTE_3d
  TYPE(RUNOFF) :: MROUTE
END MODULE multiroute