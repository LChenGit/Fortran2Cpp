! File: GRTRML.f90
SUBROUTINE GRTRML(STRING, L)
    CHARACTER*(*) STRING
    INTEGER L
    STRING = '/dev/tty'
    L = MIN(LEN(STRING), 8)
END SUBROUTINE GRTRML

PROGRAM testGRTRML
    CHARACTER*20 STRING
    INTEGER L

    CALL GRTRML(STRING, L)
    PRINT *, STRING
    PRINT *, L
END PROGRAM testGRTRML