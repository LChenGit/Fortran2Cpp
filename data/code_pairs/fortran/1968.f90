SUBROUTINE REPLACE_CHARACTER(STRING, OLD_CHAR, NEW_CHAR)
  IMPLICIT NONE
  CHARACTER(LEN=*), INTENT(INOUT) :: STRING
  CHARACTER(LEN=1), INTENT(IN) :: OLD_CHAR, NEW_CHAR
  INTEGER :: IEND, I

  IEND = LEN(STRING)
  DO I = 1, IEND
    IF (STRING(I:I) == OLD_CHAR) THEN
      STRING(I:I) = NEW_CHAR
    END IF
  END DO
END SUBROUTINE REPLACE_CHARACTER