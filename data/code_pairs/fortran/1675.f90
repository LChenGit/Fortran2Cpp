SUBROUTINE RFFTI1(N, WA, FAC)
  INTEGER, INTENT(IN) :: N
  REAL, INTENT(OUT) :: WA(N), FAC(15)
  INTEGER :: NTRYH(4), NL, NF, J, NTRY, NQ, NR
  DATA NTRYH /4,2,3,5/

  NL = N
  NF = 0
  J = 0

101 CONTINUE
  J = J + 1
  IF (J > 4) THEN
    NTRY = NTRY + 2
  ELSE
    NTRY = NTRYH(J)
  END IF

  NQ = NL / NTRY
  NR = NL - NTRY * NQ
  IF (NR /= 0) GOTO 101

  NF = NF + 1
  FAC(NF + 2) = NTRY
  NL = NQ

  IF (NL /= 1) GOTO 101
  FAC(1) = N
  FAC(2) = NF

END SUBROUTINE RFFTI1

PROGRAM MAIN
  IMPLICIT NONE
  INTEGER, PARAMETER :: N = 5
  REAL :: WA(N), FAC(15)
  INTEGER :: I

  CALL RFFTI1(N, WA, FAC)

  PRINT *, 'WA:'
  DO I = 1, N
     PRINT *, WA(I)
  END DO

  PRINT *, 'FAC:'
  DO I = 1, 15
     PRINT *, FAC(I)
  END DO
END PROGRAM MAIN