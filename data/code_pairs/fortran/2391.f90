PROGRAM TestPAKDSP
    IMPLICIT NONE

    REAL :: PAK
    CHARACTER*1 :: SGNA
    INTEGER :: DEGS, MINS
    REAL :: SECS

    ! Test case
    PAK = -123456789.0
    CALL PAKDSP(PAK, SGNA, DEGS, MINS, SECS)

    PRINT *, 'Test case: PAK = ', PAK
    PRINT *, 'SGNA: ', SGNA
    PRINT *, 'DEGS: ', DEGS
    PRINT *, 'MINS: ', MINS
    PRINT *, 'SECS: ', SECS

END PROGRAM TestPAKDSP

SUBROUTINE PAKDSP(PAK, SGNA, DEGS, MINS, SECS)
    IMPLICIT NONE
    REAL, INTENT(IN) :: PAK
    CHARACTER*1, INTENT(OUT) :: SGNA
    INTEGER, INTENT(OUT) :: DEGS, MINS
    REAL, INTENT(OUT) :: SECS
    REAL :: CON
    CHARACTER*1 :: IBLANK, NEG
    REAL, PARAMETER :: ZERO=0.0, CON1=1000000.0, CON2=1000.0, TOL=1.0E-4

    IBLANK = ' '
    NEG = '-'
    SGNA = IBLANK
    IF (PAK .LT. ZERO) SGNA = NEG
    CON = ABS(PAK)
    DEGS = INT((CON / CON1) + TOL)
    CON = MOD(CON, CON1)
    MINS = INT((CON / CON2) + TOL)
    SECS = MOD(CON, CON2)

END SUBROUTINE PAKDSP