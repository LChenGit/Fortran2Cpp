MODULE CHR_ACHR_MOD
  IMPLICIT NONE
CONTAINS
  CHARACTER(len=1) FUNCTION CHR_ACHR(ASCII)
    INTEGER, INTENT(IN) :: ASCII
    INTEGER, PARAMETER :: DEL = 127
    INTEGER, PARAMETER :: NUL = 0
    IF (ASCII > NUL .AND. ASCII <= DEL) THEN
      CHR_ACHR = CHAR(ASCII)
    ELSE
      CHR_ACHR = CHAR(NUL)
    END IF
  END FUNCTION CHR_ACHR
END MODULE CHR_ACHR_MOD