FUNCTION CLOCKF (XX)
  REAL :: CLOCKF, XX
  INTEGER :: COUNT_NOW, COUNT_RATE, COUNT_MAX
  CALL SYSTEM_CLOCK(COUNT_NOW, COUNT_RATE, COUNT_MAX)
  CLOCKF = COUNT_NOW / COUNT_RATE
END FUNCTION CLOCKF