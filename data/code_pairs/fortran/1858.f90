MODULE MESHFEM3D_MODEL_CRUST__genmod
  IMPLICIT NONE
  CONTAINS
    SUBROUTINE MESHFEM3D_MODEL_CRUST(LAT,LON,R,VPC,VSC,RHOC,MOHO,FOUND_CRUST,ELEM_IN_CRUST)
      REAL(KIND=8), INTENT(IN) :: LAT
      REAL(KIND=8), INTENT(IN) :: LON
      REAL(KIND=8), INTENT(IN) :: R
      REAL(KIND=8), INTENT(OUT) :: VPC
      REAL(KIND=8), INTENT(OUT) :: VSC
      REAL(KIND=8), INTENT(OUT) :: RHOC
      REAL(KIND=8), INTENT(OUT) :: MOHO
      LOGICAL(KIND=4), INTENT(OUT) :: FOUND_CRUST
      LOGICAL(KIND=4), INTENT(IN) :: ELEM_IN_CRUST

      VPC = LAT + LON + R
      VSC = LAT * LON * R
      RHOC = LAT - LON - R
      MOHO = LAT / LON / R
      FOUND_CRUST = ELEM_IN_CRUST
    END SUBROUTINE MESHFEM3D_MODEL_CRUST
END MODULE MESHFEM3D_MODEL_CRUST__genmod

