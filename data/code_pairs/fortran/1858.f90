  REAL(KIND=8) :: LAT, LON, R, VPC, VSC, RHOC, MOHO
  LOGICAL(KIND=4) :: FOUND_CRUST, ELEM_IN_CRUST

  ! Test case
  LAT = 45.0D0
  LON = 90.0D0
  R = 6371.0D0
  ELEM_IN_CRUST = .TRUE.

  CALL MESHFEM3D_MODEL_CRUST(LAT, LON, R, VPC, VSC, RHOC, MOHO, FOUND_CRUST, ELEM_IN_CRUST)

  PRINT *, 'Test case 1 results:'
